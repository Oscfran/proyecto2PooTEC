    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        try {
            int value = (Integer) nmVuelos.getValue();
            if (value <= 0)
                JOptionPane.showMessageDialog(this, "El nÃºmero tiene que ser mayor a 0");
            else{
                refCliente.hiloCliente.writer.writeInt(2);
                refCliente.hiloCliente.writer.writeUTF(nmVuelos.getValue().toString());
                Random rand = new Random();
                for (int i = 0;i < value;i++){
                    refCliente.hiloCliente.writer.writeInt(3);
                    refCliente.hiloCliente.writer.writeInt(rand.nextInt(3));
                    refCliente.hiloCliente.writer.writeInt(rand.nextInt(2));
                }
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "No se puede");
        }
        
        
        
    }//GEN-LAST:event_btnCrearActionPerformed

